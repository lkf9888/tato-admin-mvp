/**
 * Run the Simplified-to-Traditional substitution on a single string. Each
 * character is mapped independently — won't catch context-sensitive forms,
 * but handles the overwhelming majority of admin UI text.
 */
export function convertScToTc(input: string): string {
  if (!input) return input;
  let out = "";
  for (let i = 0; i < input.length; i += 1) {
    const ch = input[i];
    out += SC_TO_TC[ch] ?? ch;
  }
  return out;
}

/**
 * Recursively convert every string value in a nested object/array structure
 * from Simplified to Traditional Chinese. Functions and other non-string
 * primitives are passed through unchanged. Used to compute the zh-Hant
 * messages block from the zh block at module load.
 */
export function convertMessagesScToTc<T>(value: T): T {
  if (typeof value === "string") {
    return convertScToTc(value) as unknown as T;
  }
  if (Array.isArray(value)) {
    return value.map((item) => convertMessagesScToTc(item)) as unknown as T;
  }
  if (value && typeof value === "object") {
    const entries = Object.entries(value as Record<string, unknown>).map(
      ([key, val]) => [key, convertMessagesScToTc(val)],
    );
    return Object.fromEntries(entries) as unknown as T;
  }
  return value;
}
